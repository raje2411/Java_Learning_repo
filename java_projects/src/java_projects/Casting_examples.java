package java_projects;

public class Casting_examples {

	public static void main(String args[]) {

		int x = 14, y = 5;

		System.out.println("Without Casting y(" + y + ")/x(" + x + ")  is: " + (y / x));

		System.out.println("With Casting the result as double y(" + y + ")/x(" + x + ")  is: " + (double) (y / x));

	}

}
